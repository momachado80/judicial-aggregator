def get_nome_comarca(codigo: str, tribunal: str) -> str:
    """
    Retorna o nome da comarca pelo código
    
    Args:
        codigo: Código da comarca (ex: "0019", "0114")
        tribunal: "TJSP" ou "TJBA"
    
    Returns:
        Nome da comarca ou "Comarca {codigo}" se não encontrado
    """
    if tribunal == "TJSP":
        return TJSP_COMARCAS.get(codigo, f"Comarca {codigo}")
    elif tribunal == "TJBA":
        return TJBA_COMARCAS.get(codigo, f"Comarca {codigo}")
    return f"Comarca {codigo}"


def extrair_codigo_comarca(numero_processo: str) -> str:
    """
    Extrai o código da comarca do número do processo CNJ
    
    Formato CNJ: NNNNNNN-DD.AAAA.J.TR.OOOO
    Exemplo: 1003711-15.2025.8.26.0650
                                    ^^^^- Código da comarca
    
    Args:
        numero_processo: Número do processo no formato CNJ
    
    Returns:
        Código da comarca (4 dígitos)
    """
    try:
        partes = numero_processo.replace('-', '.').split('.')
        if len(partes) >= 6:
            return partes[-1].zfill(4)  # Garante 4 dígitos
        return "0000"
    except:
        return "0000"
